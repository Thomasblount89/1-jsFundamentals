// addition assignment operator
r += 1; // r = r + 1
console.log(r); //6

// substraction assignment operator
r -= 1; //r = r-1 
console.loglog(r); // 5  ( remember this reads from top to bottom , so this is why its 5)

//multiplication assignment operator
r *= 1; //r = r* 1
console.log(r); //5

//Division assignemnt Operator 
r /= 2; // r = r/2
console.log(r); //2.5


